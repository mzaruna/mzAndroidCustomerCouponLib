package com.mezzofy.mzcustomercouponlib.Data;
public class Summary {
    private float filteredsize;
    private float resultsize;
    private float totalsize;


    // Getter Methods

    public float getFilteredsize() {
        return filteredsize;
    }

    public float getResultsize() {
        return resultsize;
    }

    public float getTotalsize() {
        return totalsize;
    }

    // Setter Methods

    public void setFilteredsize(float filteredsize) {
        this.filteredsize = filteredsize;
    }

    public void setResultsize(float resultsize) {
        this.resultsize = resultsize;
    }

    public void setTotalsize(float totalsize) {
        this.totalsize = totalsize;
    }
}